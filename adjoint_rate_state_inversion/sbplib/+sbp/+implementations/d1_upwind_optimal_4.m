function [H, HI, Dp, Dm, e_1, e_m] = d1_upwind_optimal_4(m,h)
    
    if(m<4)
        error('Operator requires at least 4 grid points');
    end

    Hv=ones(m,1);
    Hv(1:4)=[0.20201235833538655575e0; 0.99538487816327991298e0; 0.97015701358216195675e0;0.10102063182719530240e1;];
    Hv(m-3:m)=rot90(Hv(1:4),2);
    Hv = Hv*h;
    H = spdiag(Hv,0);
    HI = spdiag(1./Hv,0);

    q_diags   = [-1 0 1 2 3];
    q_stencil = [-0.1e1 / 0.4e1 -0.5e1 / 0.6e1 0.3e1 / 0.2e1 -0.1e1 / 0.2e1 0.1e1 / 0.12e2;];
    Qp = stripeMatrix(q_stencil, q_diags,m);

    Q_U =[-0.1641609359635854015e-1 0.72010153330466037900e0 -0.25937677264959908880e0 0.55691332941297249787e-1;
        -0.64634991662594011739e0 -0.12450155260949913717e0 0.10677218804881020074e1 -0.38020374458599608629e0;
        0.19979027552959356602e0 -0.68387150771131856902e0 -0.47073785324634142376e0 0.13714857520947330931e1;
        -0.37024265307294908617e-1 0.88271527016157327039e-1 -0.33760725459216149519e0 -0.79697334045003425665e0;
    ];

    Qp(1:4,1:4)=Q_U;
    Qp(m-3:m,m-3:m)=rot90(Q_U,2)'; %%% This is different from standard SBP

    Qm=-Qp';

    e_1=sparse(m,1);e_1(1)=1;
    e_m=sparse(m,1);e_m(m)=1;

    Dp=HI*(Qp-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;

    Dm=HI*(Qm-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;
end

