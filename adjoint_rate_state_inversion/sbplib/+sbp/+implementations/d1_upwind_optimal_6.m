function [H, HI, Dp, Dm, e_1, e_m] = d1_upwind_optimal_6(m,h)
    
    if(m<4)
        error('Operator requires at least 4 grid points');
    end

    Hv=ones(m,1);
    Hv(1:6)=[0.13453549873457518067e0; 0.72883719241678855783e0; 0.96394950911309137162e0; 0.10118289039394886849e1; 0.99585149159164028231e0; 0.10007099932662262006e1];
    Hv(m-5:m)=rot90(Hv(1:6),2);
    Hv = Hv*h;
    H = spdiag(Hv,0);
    HI = spdiag(1./Hv,0);

    q_diags   = [-2 -1 0 1 2 3 4];
    q_stencil = [0.1e1 / 0.30e2 -0.2e1 / 0.5e1 -0.7e1 / 0.12e2 0.4e1 / 0.3e1 -0.1e1 / 0.2e1 0.2e1 / 0.15e2 -0.1e1 / 0.60e2;];
    Qp = stripeMatrix(q_stencil, q_diags,m);

    Q_U =[-0.527188894566819817e-2 0.68540683025212185183e0 -0.25620768386943210666e0 0.98093834445262851819e-1 -0.24478376094347018046e-1 0.24572842120626181020e-2; 
        -0.66046872227350372358e0 -0.36157928875428000723e-1 0.96186037635837315412e0 -0.36538624406042513454e0 0.11991784398196832872e0 -0.19765325130984621604e-1;
        0.22916172733719133372e0 -0.85169587117698350574e0 -0.12305320621748309991e0 0.10510321448470493093e1 -0.41033200739782096774e0 0.12155387927471359378e0;
        -0.79272394288614277257e-1 0.25636096505883893017e0 -0.71256809158502466194e0 -0.31452956876311019126e0 0.12063076245870116394e1 -0.47296520167576810278e0;
        0.17157029288775200943e-1 -0.60563023212787194176e-1 0.14589211447828370901e0 -0.52316776169335319560e0 -0.51048454683932583137e0 0.13144995213117406447e1;
        -0.13057511181803376595e-2 0.66490279542379226875e-2 -0.15923509164716998956e-1 0.53957595224576363972e-1 -0.41426387157081948428e0 -0.57911349132509746586e0;
    ];

    Qp(1:6,1:6)=Q_U;
    Qp(m-5:m,m-5:m)=rot90(Q_U,2)'; %%% This is different from standard SBP

    Qm=-Qp';

    e_1=sparse(m,1);e_1(1)=1;
    e_m=sparse(m,1);e_m(m)=1;

    Dp=HI*(Qp-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;

    Dm=HI*(Qm-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;
end

