function [H, HI, Dp, Dm, e_1, e_m] = d1_upwind_optimal_9(m,h)

if(m<4)
    error('Operator requires at least 4 grid points');
end

Hv=ones(m,1);
Hv(1:8)=[0.13390365219997335565e0; 0.73561691942079016740e0; 0.99121041006864219646e0; 0.99682029813837585962e0; 0.10026525038242640599e1; 0.99885711718504812105e0; 0.10002830366448330126e1; 0.99996864791153806434e0;];
