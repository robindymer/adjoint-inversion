function [H, HI, Dp, Dm, e_1, e_m] = d1_upwind_optimal_7(m,h)
    
    if(m<4)
        error('Operator requires at least 4 grid points');
    end

    Hv=ones(m,1);
    Hv(1:6)=[0.14051418984105811429e0; 0.74849393742828577321e0; 0.98497253950173333565e0; 0.10014890674723927762e1; 0.99984373291072125653e0; 0.10000096580285420815e1;];
    Hv(m-5:m)=rot90(Hv(1:6),2);
    Hv = Hv*h;
    H = spdiag(Hv,0);
    HI = spdiag(1./Hv,0);

    q_diags   = [-3 -2 -1 0 1 2 3 4];
    q_stencil = [-0.1e1 / 0.105e3 0.1e1 / 0.10e2 -0.3e1 / 0.5e1 -0.1e1 / 0.4e1 1 -0.3e1 / 0.10e2 0.1e1 / 0.15e2 -0.1e1 / 0.140e3;];
    Qp = stripeMatrix(q_stencil, q_diags,m);

    Q_U =[-0.228173095693452681e-2 0.67254655806120689750e0 -0.23729579990959587454e0 0.82956676738938167554e-1 -0.16890499525210505835e-1 0.96479559159584037454e-3;
        -0.66161134719830224075e0 -0.15739824151270088653e-1 0.91101642355446486786e0 -0.30050454729370870944e0 0.75545907673056920417e-1 -0.87066125842407369795e-2;
        0.22500418364202798429e0 -0.86275907886792471561e0 -0.53631964120828718339e-1 0.91026559149168032315e0 -0.27522310585295343623e0 0.63487230850855708944e-1;
        -0.74061324569180681349e-1 0.25253662232113023800e0 -0.76274825081992406554e0 -0.13670157389509170175e0 0.95279176260492901387e0 -0.29134104516567232405e0;
        0.13341685349479031887e-1 -0.49593984960165282371e-1 0.16062419295811737103e0 -0.65773490747679794089e0 -0.21951590605341724270e0 0.99335511065897453920e0;
        -0.39146626708956589435e-3 0.30097075970229570375e-2 -0.17964601662233573343e-1 0.11124256995878938786e0 -0.60718434932259522577e0 -0.24823566982770350367e0;];

    Qp(1:6,1:6)=Q_U;
    Qp(m-5:m,m-5:m)=rot90(Q_U,2)'; %%% This is different from standard SBP

    Qm=-Qp';

    e_1=sparse(m,1);e_1(1)=1;
    e_m=sparse(m,1);e_m(m)=1;

    Dp=HI*(Qp-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;

    Dm=HI*(Qm-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;
end

