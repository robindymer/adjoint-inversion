function [H, HI, Dp, Dm, e_1, e_m] = d1_upwind_optimal_5(m,h)
    
    if(m<4)
        error('Operator requires at least 4 grid points');
    end

    Hv=ones(m,1);
    Hv(1:4)=[0.19075305447003437120e0; 0.91903270497798828900e0; 0.10121982753809732052e1; 0.99849755697400857621e0;];
    Hv(m-3:m)=rot90(Hv(1:4),2);
    Hv = Hv*h;
    H = spdiag(Hv,0);
    HI = spdiag(1./Hv,0);

    q_diags   = [-2 -1 0 1 2 3];
    q_stencil = [0.1e1 / 0.20e2 -0.1e1 / 0.2e1 -0.1e1 / 0.3e1 1 -0.1e1 / 0.4e1 0.1e1 / 0.30e2;];
    Qp = stripeMatrix(q_stencil, q_diags,m);

    Q_U =[-0.642001858038057020e-2 0.67075332356177613625e0 -0.20519536518316388147e0 0.40862060201768315471e-1;
        -0.64349108585811824992e0 -0.45608545701177496633e-1 0.89177426473822554933e0 -0.23600796651226313612e0;
        0.18431797079368919162e0 -0.74744694065397553192e0 -0.18363957690970180930e0 0.96343521343665481638e0;
        -0.34406866355190371522e-1 0.12230216279337689235e0 -0.55293932264535985850e0 -0.31828930712615999565e0;
     ];

    Qp(1:4,1:4)=Q_U;
    Qp(m-3:m,m-3:m)=rot90(Q_U,2)'; %%% This is different from standard SBP

    Qm=-Qp';

    e_1=sparse(m,1);e_1(1)=1;
    e_m=sparse(m,1);e_m(m)=1;

    Dp=HI*(Qp-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;

    Dm=HI*(Qm-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;
end

