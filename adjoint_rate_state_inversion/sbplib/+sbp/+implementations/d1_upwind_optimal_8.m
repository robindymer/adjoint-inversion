function [H, HI, Dp, Dm, e_1, e_m] = d1_upwind_optimal_8(m,h)

if(m<4)
    error('Operator requires at least 4 grid points');
end

Hv=ones(m,1);
Hv(1:8)=[0.13507290557309337313e0; 0.74352490902031070675e0; 0.99441614640345370793e0; 0.99628713916485302067e0; 0.10023473699353751236e1; 0.99921404886576951154e0; 0.10001371623119208433e1; 0.99999159495728280116e0;];
Hv(m-7:m)=rot90(Hv(1:8),2);
Hv = Hv*h;
H = spdiag(Hv,0);
HI = spdiag(1./Hv,0);

q_diags   = [-3 -2 -1 0 1 2 3 4 5];
q_stencil = [-0.1e1 / 0.168e3 0.1e1 / 0.14e2 -0.1e1 / 0.2e1 -0.9e1 / 0.20e2 0.5e1 / 0.4e1 -0.1e1 / 0.2e1 0.1e1 / 0.6e1 -0.1e1 / 0.28e2 0.1e1 / 0.280e3;];
Qp = stripeMatrix(q_stencil, q_diags,m);

Q_U =[-0.11370850492337153867e0 0.10005566862763054583e1 -0.81582454111942301005e0 0.85353331421608316295e0 -0.70489159564056297784e0 0.38668324007024262222e0 -0.12380973122454489911e0 0.17461132345271192469e-1;
    -0.33722500953196562367e0 -0.96843533055544413737e0 0.25949013168738898626e1 -0.25609457515764766126e1 0.21105754197636806337e1 -0.11541520721698739715e1 0.36618311800252256023e0 -0.50901690806332726936e-1;
    -0.36159837551366945241e0 0.84888753275751564560e0 -0.30711619912094547786e1 0.49874201459239552129e1 -0.39824395250068253601e1 0.21678540040293521688e1 -0.68279930309448383404e0 0.93837512113610370122e-1;
    0.75925344617425794361e0 -0.21624987320959014292e1 0.34651403589374203065e1 -0.58455509877775033440e1 0.61699766368118273090e1 -0.32903267424399246333e1 0.10596900287313222827e1 -0.15925543691292690750e0;
    -0.78113978731968276511e0 0.22449351457020418961e1 -0.38360841206648273447e1 0.47232204878378227749e1 -0.51491138350387529750e1 0.38818067557047204629e1 -0.13319078662031583339e1 0.28042607712469353336e0;
    0.46537769859334289965e0 -0.13436744579219883964e1 0.23292112012911994387e1 -0.30451659068758007138e1 0.22903993786806181110e1 -0.19993492448878799124e1 0.17338667013469498723e1 -0.56518917975025078669e0;
    -0.15215044240870532568e0 0.44248385889443441096e0 -0.77717343107307775616e0 0.10380915983356246251e1 -0.86390287679052109730e0 0.1645833197999373945e-1 -0.60941756441364717214e0 0.12710867159520890522e1;
    0.21190974929793858915e-1 -0.62254703056963391600e-1 0.11099120696427339574e0 -0.15060290008370508106e0 0.12939639722053636949e0 -0.3021891334249524390e-2 -0.47728157362115094966e0 -0.45294132054234420033e0;
    ];

Qp(1:8,1:8)=Q_U;
Qp(m-7:m,m-7:m)=rot90(Q_U,2)'; %%% This is different from standard SBP

Qm=-Qp';

e_1=sparse(m,1);e_1(1)=1;
e_m=sparse(m,1);e_m(m)=1;

Dp=HI*(Qp-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;

Dm=HI*(Qm-1/2*(e_1*e_1')+1/2*(e_m*e_m')) ;
end

